        WHEN 'ZACATECAS' THEN curp := CONCAT(curp, 'ZS');
        ELSE curp := CONCAT(curp, 'NE');
    END CASE;
    
    -- Segunda consonante del apellido paterno
    FOR indx IN 2 .. LENGTH(apellido_pat) LOOP
        IF SUBSTR(apellido_pat, indx, 1) = 'a' OR SUBSTR(apellido_pat, indx, 1) = 'e' OR SUBSTR(apellido_pat, indx, 1) = 'i' OR
           SUBSTR(apellido_pat, indx, 1) = 'o' OR SUBSTR(apellido_pat, indx, 1) = 'u' THEN
            CONTINUE;
        ELSE        
            curp := CONCAT(curp, SUBSTR(apellido_pat, indx, 1));
            EXIT;
        END IF;
    END LOOP;
    
    -- Segunda consonante del apellido materno
    FOR indx IN 2 .. LENGTH(apellido_mat) LOOP
        IF SUBSTR(apellido_mat, indx, 1) = 'a' OR SUBSTR(apellido_mat, indx, 1) = 'e' OR SUBSTR(apellido_mat, indx, 1) = 'i' OR
