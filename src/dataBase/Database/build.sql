        WHEN 'OAXACA' THEN curp := CONCAT(curp, 'OC');
        WHEN 'QUERETARO' THEN curp := CONCAT(curp, 'QT');
        WHEN 'SAN LUIS POTOSI' THEN curp := CONCAT(curp, 'SP');
        WHEN 'SLP' THEN curp := CONCAT(curp, 'SP');
