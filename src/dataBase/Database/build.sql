  CREATE OR REPLACE EDITIONABLE FUNCTION "USR_1"."GET_CURP" (nombre VARCHAR2, apellido_pat VARCHAR2, apellido_mat VARCHAR2, fecha_nacimiento DATE, estado VARCHAR2, sexo VARCHAR2)
RETURN VARCHAR2 IS
curp VARCHAR2(16);
indx NUMERIC;
BEGIN    
    -- Primera letra apellido paterno
    curp := CONCAT(curp, SUBSTR(apellido_pat, 1, 1));    
    
    -- Primera vocal apellido paterno
    IF SUBSTR(apellido_pat, 2, 1) = 'a' OR SUBSTR(apellido_pat, 2, 1) = 'e' OR SUBSTR(apellido_pat, 2, 1) = 'i' OR
       SUBSTR(apellido_pat, 2, 1) = 'o' OR SUBSTR(apellido_pat, 2, 1) = 'u' THEN
        curp := CONCAT(curp, SUBSTR(apellido_pat, 2, 1));
    ELSE        
        curp := CONCAT(curp, SUBSTR(apellido_pat, 3, 1));
    END IF;
    
    -- Primera letra apellido materno
    curp := CONCAT(curp, SUBSTR(apellido_mat, 1, 1));    
    
    -- Primera letra nombre (Exceptuando si son dos nombres y el primero es Maria o Jose)
    IF SUBSTR(RTRIM(nombre), 1, 6) = 'Maria ' OR  SUBSTR(RTRIM(nombre), 1, 5) = 'Jose ' THEN
        indx := 6;
        LOOP EXIT WHEN (SUBSTR(nombre, indx, 1) like UPPER(SUBSTR(nombre, indx, 1)) AND NOT SUBSTR(nombre, indx, 1) = ' ');
            indx := indx + 1;
        END LOOP;
    curp := CONCAT(curp, SUBSTR(nombre, indx, 1));  -- Encontrar que esto sea solo si la letra es mayuscula (es decir, es el inicio de un apellido)
    ELSE
        curp := CONCAT(curp, SUBSTR(nombre, 1, 1));  
    END IF;
    
    -- Digitos de la fecha de nacimiento
    curp := CONCAT(curp, TO_CHAR(fecha_nacimiento, 'YYMMDD'));
    
    -- Letra de genero 
    IF sexo LIKE '%ombre' OR sexo LIKE '%asculino' THEN
        curp := CONCAT(curp, 'H');
    ELSE
        curp := CONCAT(curp, 'M');
    END IF;
    
    -- Dos letras del estado
    CASE UPPER(estado)
        WHEN 'AGUASCALIENTES' THEN curp := CONCAT(curp, 'AS');
        WHEN 'BAJA CALIFORNIA SUR' THEN curp := CONCAT(curp, 'BS');
        WHEN 'BCS' THEN curp := CONCAT(curp, 'BS');
        WHEN 'COAHUILA' THEN curp := CONCAT(curp, 'CL');
