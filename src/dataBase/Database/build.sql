            EXIT;
        END IF;
    END LOOP;
    
    -- Segunda consonante del nombre (ignorando Maria y Jose en nombres compuestos)
    IF SUBSTR(RTRIM(nombre), 1, 6) = 'Maria ' OR  SUBSTR(RTRIM(nombre), 1, 5) = 'Jose ' THEN
