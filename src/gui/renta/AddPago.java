    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Accion del boton aceptar
        // Verifica si se selecciono un elemento de la tabla
        if (tableList.getSelectedRow() < 0){
            // Suena un beep
            Toolkit.getDefaultToolkit().beep();
            // Muestra un mensage de aviso
            JOptionPane.showMessageDialog(this, "Seleccione un tipo de pago.", "Aviso",2);            
        }
        else if (txtMonto.getText().trim().length() == 0){
             // Suena un beep
            Toolkit.getDefaultToolkit().beep();
            // Muestra un mensage de aviso
            JOptionPane.showMessageDialog(this, "Escriba el monto.", "Aviso",2);    
            txtMonto.requestFocus();
        }
        else{      
            // Toma el id y guarda el actor en a tabla de casting y guarda el idioma
            pago = new Object[3];
            pago[0]= tPagosLista[tableList.getSelectedRow()][0].toString();
            pago [1] = tPagosLista[tableList.getSelectedRow()][1].toString();
            pago [2] = txtMonto.getText();
            dispose();                 
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

